package com.question1;

public class PatternPrinting_Starts {

	public static void main(String[] args) {
		int number = 5;

		for (int rowNumber = 1; rowNumber <= number; rowNumber++) {

			for (int colNumber = 1; colNumber <= rowNumber; colNumber++) {
				
				

				System.out.print("*");
				
				
				
			}
			System.out.println("  ");
		}

	}

}
